# Subscripting
print("Hello"[0])

# String Concatenation
print("123" + "345")

# Integer = Whole number = Basic Addition
print(123 + 345)

# Large Integers -- Python will interpret it as a full whole number neglecting the '_'
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)
